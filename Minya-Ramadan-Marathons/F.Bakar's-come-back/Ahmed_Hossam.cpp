void Solve(){
    // Declare an integer variable named "n"
    ll n;
    // Read an integer from the standard input and assign it to "n"
    cin >> n;
    // If "n" is odd and not equal to 1
    if(n & 1 && n != 1)
        // Output a string consisting of "1" followed by "0"s, the number of which is equal to "(n - 3) / 2"
        cout << "1" + string(max(0ll, (n - 3) / 2), '0') << '\n';
    // Otherwise
    else 
        // Output -1
        cout << -1 << '\n';
}

