template < typename T = int > ostream& operator << (ostream &out, const vector < T > &v) { 
    for (const T &x : v) out << x << ' '; 
    return out;
}

void Solve(){
    // Declare a string variable named s and get its value from the user.
    string s;
    cin >> s;

    // Declare a vector of integers named cnt with size 26.
    vector < int > cnt(26);

    // Iterate over each character in the string s and increment the corresponding
    // integer in the vector cnt by 1.
    for(auto &c : s) 
        cnt[c - 'a']++;

    // Declare an integer variable named len and initialize it to 0.
    int len = 0;

    // Iterate over each integer in the vector cnt and update len to be the maximum value.
    for(auto &x : cnt) 
        len = max(len, x);

    // Output -1 if len is less than 2, otherwise output len.
    cout << (len < 2 ? -1 : len) << '\n';
}

int main(){
    ios_base::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    int test_cases = 1;
    cin >> test_cases;
    for(int tc = 1; tc <= test_cases; tc++){
        // cout << "Case #" << tc << ": ";
        Solve();
    }
    return 0;
}
